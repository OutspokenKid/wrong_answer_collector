

class WrongAnswers:

    def __init__(self, item, wrong_answers_string_dict) -> None:

        self.item = item
        union_set = set()

        for name in wrong_answers_string_dict.keys():
            wrong_answer_string = wrong_answers_string_dict[name]

            if wrong_answer_string:
                print(f"이름 : {name}")
            else:
                print(f"{name} 제출 안함.")
                continue

            split_word = ""

            if "," in wrong_answer_string:
                split_word = ","
            elif "." in wrong_answer_string:
                split_word = "."
            elif "/" in wrong_answer_string:
                split_word = "/"
            else:
                split_word = " "

            temp_list = wrong_answer_string.split(split_word)
            wrong_answer_set = set()

            # 공백 삭제.
            for wrong_answer in temp_list:

                # 빈 문자열일 경우 패스.
                if wrong_answer:
                    clean_word = wrong_answer.strip()
                    wrong_answer_set.add(clean_word)
                    union_set.add(int(clean_word))

        self.union_list = list(union_set)
        self.union_list.sort()
        print(f"\n오답의 합집합. 총 {len(self.union_list)}문제\n{self.union_list}")


